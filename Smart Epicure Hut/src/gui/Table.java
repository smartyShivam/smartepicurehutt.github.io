    private void getEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)jTable1.getModel();
            //remove rows from jtable
            int rc=dtm.getRowCount();
            for(int a=1;a<=rc;a++){
                dtm.removeRow(0);
            }
            ResultSet rs=db.DbConnect.st.executeQuery("select * from table_number");
            int x=1;
            while(rs.next()){
                String n=rs.getString("table_name");
                java.util.Vector r=new java.util.Vector();
                r.add(x++);
                r.add(n);
                dtm.addRow(r);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

